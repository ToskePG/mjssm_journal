<p><u><b>1.3. Title</b></u></p>

<p>Authors should include all information in the title that will make electronic retrieval of the article both sensitive and specific.</p>

<p>Check spelling and language of your title. The title should be in Titlecase, written in uppercase and lowercase letters (initial uppercase for all words except articles, conjunctions, short prepositions no longer than four letters etc.) so that first letters of the words in the title are in caps. Exceptions are words like: articles, and, or, between etc. The word following a colon (:) or a hyphen (-) in the title is always capitalized.</p>

<p><b>Example:</b> </p>

<p>Effects of Socio-Economic and Behavioral Characteristics in Explaining Central Obesity – A Study of Adult Asian Americans in San Diego, USA</p>



<p><u><b>1.4. Type of publication</b></u></p>

<p>Authors should suggest the type of their submission. Montenegrin Journal of Sports Science and Medicine publishes original scientific papers, notes, preliminary communications, reviews and conference papers.</p>

<p><b>Original scientific papers</b> – report hitherto unpublished results of original research. The acceptance of the paper obliges the author not to publish the same material elsewhere without the permission of the Editorial Board.</p>

<p><b>Notes (short communications)</b> – reports on shorter but completed research.</p>

<p><b>Preliminary communications</b> – include preliminary results of greater importance requiring rapid publication.</p>

<p><b>Reviews</b> – original, concise and critical surveys of a current research area in which the author himself (authors themselves are) is active. In the review the role of the author's contribution in this field in relation to other published reports, as well as original views should be given.</p>

<p><b>Conference papers</b> – previously reported at congress, symposium or summer school, etc., should be submitted by the Organizing Committee in the form of a complete manuscript of the Proceedings</p>


<p><u><b>1.5. Running head</b></u></p>

<p>Short running title should not exceed 50 characters including spaces.</p>

<p><b>Example: </b></p>

<p>Attitudes toward advertising through sport</p>

<p>(Title: Attitudes toward advertising through sport among the frequency of watching sports events)</p>


<p><u><b>1.6. Authors</b></u></p>

<p>In one line list all authors with full names separated by a comma (and space). Last author should be separated with the word "and". Only the first name, initial of a middle and family name of authors should be given. <b>Avoid any abbreviations of academic or professional titles</b>. If authors belong to different institutions, following a family name of the author there should be a number in superscript designating affiliation (see the next section 1.7. Affiliations).</p>

<p><b>Example: </b></p>

<p>Selcuk Akpinar<sup>1</sup>, Stevo Popović<sup>1,2</sup> and Sadettin Kirazci<sup>1</sup></p>


<p><u><b>1.7. Affiliations</b></u></p>
<p>Affiliation consists of the name of a department(s) and institution(s), city and country to which the author(s) belong and to which the presented / submitted work should be attributed. </p>
<p>List all affiliations (each in a separate line) in the order corresponding to the list of authors. Affiliations must be written in English, so carefully check the official English translation of the names of institutions and departments. Do not translate yourself – check the information since Editorial Board does not have any way of checking these. </p>
<p>Affiliations should be written as follows: </p>
<p><b>name of department(s), name of institution(s), town, state</b></p>
<p>Only if there is more then one affiliation, should a number be given to each affiliation in order of appearance. This number should be written <b>in</b> <b>superscript</b> <b>at</b> <b>the</b> <b>beginning</b> of the line, separated from corresponding affiliation with a space. This number should also be put <b>after</b> corresponding name of the author, in superscript with <b>no</b> <b>space</b> in between (see example).</p>
<p>If an author belongs to more than one institution, all corresponding superscript digits, separated with a comma with <b>no</b> <b>space</b> in between, should be present behind the family name of this author.</p>
<p>In case all authors belong to the same institution affiliation numbering is not needed.</p>
<p>Whenever possible expand your authors' affiliations with departments, or some other, specific and lower levels of organization.</p>
<p><b>Example:</b></p>

<p>Selcuk Akpinar<sup>1</sup>, Stevo Popovic<sup>1,2</sup> and Sadettin Kirazci<sup>1</sup></p>

<p><sup>1</sup>Physical Education and Sports Department, Middle East Technical University, Ankara, Turkey</p>

<p><sup>2</sup>Faculty for Sport and Physical Education, University of Montenegro, Niksic, Montenegro </p>


<p><u><b>1.8. Abstract</b></u></p>
<p>The abstract should provide the context or background for the study and should state the study’s purposes, basic procedures (selection of study subjects or laboratory animals, observational and analytical methods), main findings (giving specific effect sizes and their statistical significance, if possible), and principal conclusions. It should emphasize new and important aspects of the study or observations. </p>
<p>It is recommended that abstract should not exceed one typed double-spaced page, written in English. It should be placed, <b>in italics</b>, on second page of your document after the standard title written in caps (<b>abstract</b>).</p>
<p>Since abstract is independent part of your paper, all <b>abbreviations </b>used in the abstract should also be <b>explained </b>in it. If an abbreviation is used, the term should always be first written in full with the abbreviation in parentheses immediately after it.</p>
<p>Abstract <b>should not </b>have any special headings (Aim, Results…). Abstract should be written as a continuous text and consists of <b>one paragraph only</b>!</p>
<p>Citing literature in abstract is done in the same way as in the rest of the text.</p>

<p><u><b>1.9. Key words</b></u></p>
<p>Authors should provide 3 to 10 key words or short phrases that capture the main topics of the article. These will assist indexers in cross-indexing the article and may be published with the abstract. Terms from the <b>Medical Subject Headings (MeSH)</b> list of <b>Index Medicus</b> should be used. There are other subject headings lists available on the Internet like <b>Sociology and Anthropology Subject Headings</b>. </p>
<p>Key words should be placed on the second page of the manuscript right below the abstract in English, written in italic as shown in the following example.</p>
<p><b>Example:</b></p>

<p>ABSTRACT </p>

<p><i>Results of the analysis of............</i></p>

<p><b>Key words</b>: spatial memory, blind, transfer of learning, feedback</p>


<p><u><b>1.10. Chapters of the manuscript</b></u></p>

<p>Style of the journal prescribes only 2 types of titles:</p>

<p><b>Main chapter titles</b> – written <b>in bold</b> and <b>in titlecase </b>(initial uppercase for all word except articles, conjunctions, short prepositions etc.). The word following a colon (:) or a hyphen (-) in the title is always capitalized.</p>

<p><b>Example:</b></p>

<p><b>Methods of Analysis – An Overview</b></p>

<p><b>Subtitles</b> – all other titles within one chapter. They are written <b>in italic </b>and in normal <b>sentence</b> <b>case</b> (only the starting word of a title begins with first letter in caps).</p>

<p><b>Example:</b></p>

<p><i>Table position of the research football team</i></p>
<p>Note that titles but NOT the subtitles are written in titlecase. </p>

<p>Do not put a full stop or any other sign at the end of the title.</p>

<p>Depending on the type of publication main manuscript chapters may vary. The general outline is: Introduction, Materials, Methods, Results, Discussion, Conclusion, Acknowledgements. If not needed, acknowledgements may be omitted. Conclusion can be part of Discussion. Materials and Methods can be (if authors want to) united in one chapter and titled “Materials and Methods”. Results and Discussion can be united in one chapter and titled accordingly. Subjects and Methods or Patients and Methods can also be used as a chapter title. For example, a case report might have the following titles: Introduction – Case Report – Discussion – Conclusion – Acknowledgements.</p>

<p>However, this scheme may not be suitable for reviews or publications from some areas and authors should then adjust their chapters accordingly but use the general outline as much as possible.</p>

<p><u><b>1.10.1. Introduction</b></u></p>
<p>Introduction provides a context or background for the study (i.e. the nature of the problem and its significance). It states the specific purpose or research objective of, or hypothesis tested by the study or observation; e.g. keep in mind that the research objective is often more sharply focused when stated as a question. Both the main and secondary objectives should be made clear, and any pre-specified subgroup analyses should be described. Only strictly pertinent references should be given without data or conclusions from the work being reported.</p>

<p><u><b>1.10.2. Materials (subjects, patients etc.) and Methods</b></u></p>

<p>The Materials and Methods section should include only information that was available at the time the plan or protocol for the study was written. All information obtained during the conduct of the study belongs to the Results section.</p>

<p><b>Statistics</b></p>

<p>Describe statistical methods with enough detail to enable a knowledgeable reader with access to the original data to verify the reported results. When possible, quantify findings and present them with appropriate indicators of measurement error or uncertainty (such as confidence intervals). Avoid relying solely on statistical hypothesis testing, such as the use of p-values, which fails to convey important information about effective size. References for the design of the study and statistical methods should comply with standard works when possible (with pages stated). Define statistical terms, abbreviations, and most particularly symbols. Specify the computer software used.</p>

<p><u><b>1.10.3. Results</b></u></p>

<p>Present your results in logical sequence in the text, tables, and illustrations, giving the main or most important findings first. Do not repeat in the text all the data in the tables or illustrations; emphasize or summarize only important observations. Restrict tables and figures to those needed to explain the argument of the paper and to assess its support. Use graphs as an alternative to tables with many entries; do not duplicate data in graphs and tables. Avoid non-technical uses of technical terms in statistics, such as “random” (which implies a randomizing device), “normal”, “significant”, “correlations”, and “sample”.</p>

<p><u><b>1.10.4. Discussion and Conclusion</b></u></p>

<p>Emphasize the new and important aspects of the study and the conclusions that follow from them. Do not repeat in detail data or other material given in the Introduction or the Results section. For experimental studies it is useful to begin the discussion by summarizing briefly the main findings, then explore possible mechanisms or explanations for these findings, compare and contrast the results with other relevant studies, state the limitations of the study, and explore the implications of the findings for future research and for clinical practice.</p>

<p>Link the conclusions with the goals of the study but avoid unqualified statements and conclusions not adequately supported by the data. In particular, authors should avoid making statements on economic costs and benefits unless their manuscript includes appropriate economic data and analyses. Avoid claiming priority and alluding to work that has not been completed. State new hypotheses when warranted, but clearly label them as such.</p>

<p>Authors can also separate Conclusion chapter from Discussion.</p>

<p><u><b>1.10.5. Acknowledgements</b></u></p>

<p>Authors should acknowledge help and support (financial, advisory etc.) from an institution, organization, group or an individual who have contributed to the production of the article. </p>


<p><u><b>1.11. References</b></u></p>

<p>References should be numbered consecutively in the order in which they are <b>first mentioned</b> in the text.</p>

<p>All information needed for each type of reference (journal article, book, book chapter, internet source etc.) <b>MUST</b> be present as specified in guidelines. If you omit any of the specified data you will surely be contacted to rectify it.</p>

<p>Authors are responsible for accuracy of each reference. Use authoritative source for information such as databases Current contents, Web of Science, Medline or PubMed (see <b>Links</b>) to check the validity of journal article citation.</p>

<p><b>Journal abbreviations - </b>titles of journals should be abbreviated according to their <b>official</b> abbreviations. Please, consult the list of journals indexed in Medline, Index Medicus or some other similar database.</p>

<p><u><b>1.11.1. Reference list</b></u></p>
<p>Reference list is a highly structured part of the text where each comma, space, colon, etc. counts. Authors are kindly asked to be very careful and strict when editing this part of the text. Following are some general rules and examples that should help authors.</p>
<p>In the reference list, references should be listed one after another (<b>without line break</b>) separated with a <b>dash</b> (see an example of a reference list).</p>

<p>Please note that names of journals should be given in their official (Medline, Pubmed, Index medicus) <b>abbreviated form</b>. (for example: Montenegrin Journal of Sports Science and Medicine – Monten. J. Sports Sci. & Med. – DO NOT CREATE YOUR OWN ABBREVIATION – CHECK IT, PLEASE!)</p>

<p><b>All authors</b> of the cited source should be quoted (<i>et. al</i>. cannot be used in the reference list). Accuracy and content of the references are <b>author’s responsibility</b>, so make sure you provide correct references.</p>

