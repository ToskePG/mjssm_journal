<p><u><b>1.14. Title in Montenegrin (only for Montenegrin authors)</b></u></p>

<p>Title in Montenegrin, together with an abstract in Montenegrin should be placed on a separate page following the author's address and e-mail. </p>

<p>Title in Montenegrin should be written in bold capital letters.</p>


<p><u><b>1.15. Abstract in Montenegrin (only for Montenegrin authors)</b></u></p>

<p>Only Montenegrin authors should provide the <b>title of the paper</b> and <b>abstract</b> in the Montenegrin language, which must correspond to the title and abstract in English. They should be placed at the end of the manuscript, after the author’s corresponding address.</p>

<p>There should be no subtitles in Montenegrin abstract (Aim, Results etc.). Abstract in Montenegrin just as in English should be written as in one paragraph.</p>

<p>Montenegrin authors should check the language and do the spell-check for Montenegrin.</p>

<p>Key words in Montenegrin are not needed.</p>

<p><b>Example:</b></p>

<p><b>RAZLIKE U MORFOLOSKOM STATUS MLADIH FUDBALERA I NESPORTISTA</b></p>

<p>SAŽETAK</p>

