<p><u><b>1.16. Tables</b></u></p>

<p>All tables should be included in the main manuscript file, each on a separate page right after the abstract in Montenegrin. Number tables consecutively in the order of their first citation in the text and supply a brief title for each.</p>

<p>Tables and table headings should be completely intelligible without reference to the text. Give each column a short or abbreviated heading. Authors should place explanatory matter in footnotes, not in the heading. All abbreviations appearing in a table and not considered standard MUST be explained in a footnote of that table (see <b>Abbreviations</b>).</p>

<p>Tables should be presented as standard MS Word tables, NOT as pictures or other graphic material. Do not use simple text editing to create a table, but the table option instead. If you are not familiar with using MS Word please find a colleague to help you.</p>

<p>All text appearing in tables should be written beginning only with first letter of the first word in caps (only the initial word of all text should be capitalized), i.e. all words for variable names, column headings etc. in tables should start with the first letter in caps.</p>

<p>Avoid any formatting (bold, italic, underline) in your tables and their headings. Avoid any shading or coloring in your tables. Avoid any border formatting in your tables. Put plain borders, no additional graphic editing is necessary.</p>

<p>Be sure that each table is cited in the text.<br />
If you use data from another published or unpublished source, obtain permission and acknowledge them fully.</p>

<p>The format of tables should not exceed A4. Only exceptionally the format A3 will be accepted.</p>

<p><b>Example:</b></p>

<p><b>TABLE 5</b></p>
