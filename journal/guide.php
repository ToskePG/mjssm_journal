<p><u><b>1.11.2. Reference citation</b></u></p>
<p>References should be numbered in the order in which they appear in the text. <b>Number</b> of the reference should be written in <b>superscript</b> after the text <b>without spacing and without brackets</b>. If the reference number is at the end of a line/sentence, put full stop <b>after</b> the reference. More than two references are not to be spaced. If more than two consecutive references are encountered, please separate them with a <b>dash</b>.</p>
<p><b>Examples for references in the text:</b></p>

