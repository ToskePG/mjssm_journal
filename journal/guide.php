<p><u><b>2.2. Numbers, units of measurement, variables and values</b></u></p>

<p>In Montenegrin Journal of Sports Science and Medicine all units of measures should conform to the International System of Units (SI).</p>

<p>Decimal places in English language are separated with a full stop and NOT with a comma. Thousands are separated with a comma</p>

<p>Temperatures should be expressed in degrees Celsius. Blood pressures should be expressed in millimeters of mercury, unless other units are specifically required by the journal.</p>

<p><b>Examples:</b></p>

<p>Percentages and degrees: <br />
<b>SHOULD BE:</b> 10%<br />
<b>NOT:</b> 10 % (NO space in between)</p>

<p><b>SHOULD BE:</b> 10<br />
<b>NOT:</b> 10 º (NO space in between)</p>

<p>All other units of measure: <br />
<b>SHOULD BE:</b> 10 kg<br />
<b>NOT:</b> 10kg (a space in between)</p>

<p>Ratios: <br />
<b>SHOULD BE:</b> 12:2<br />
<b>NOT:</b> 12 : 2 (NO space in between)</p>

<p>Decimal numbers: <br />
<b>SHOULD BE:</b> 0.056<br />
<b>NOT:</b> .056 (there should be a zero in front of decimal full stop)</p>

<p><b>SHOULD BE:</b> SD=3.5<br />
<b>NOT:</b> SD = 3.5 (NO space in between)</p>

<p><b>SHOULD BE:</b> X=45±3.4<br />
<b>NOT:</b> X=45 ± 3.4 (NO space in between)</p>

<p><b>SHOULD BE:</b> 45±3.4<br />
<b>NOT:</b> 45 ± 3.4 (NO space in between)</p>

<p><b>SHOULD BE:</b> X±SD (use standard abbreviation)<br />
<b>NOT:</b> Mean±SD</p>

<p><b>SHOULD BE:</b> p&lt;0.005<br />
<b>NOT:</b> p &lt; 0.005 (NO space in between)</p>

<p><b>SHOULD BE:</b> 0.001&lt;P<br />
<b>NOT:</b> 0.001 &lt; p &lt; 0.005 (NO space in between)</p>

<p><b>SHOULD BE:</b> χ<sup>2</sup><br />
<b>NOT:</b> chi square or X2 or X<sup>2</sup> (use the Greek letter)</p>
<p><b>SHOULD BE:</b> χ<sup>2</sup>=2345<br />
<b>NOT:</b> χ<sup>2 </sup>= 2345 (no space between)</p>

<p><b>SHOULD BE:</b> p<br />
<b>NOT:</b> P (not capitalized)</p>


<p><u><b>2.3. Abbreviations</b></u></p>

<p>Use only standard abbreviations; the use of non-standard abbreviations can be extremely confusing to readers. Avoid abbreviations in the title. The full term for which an abbreviation stands should precede its first use in the text unless it is a standard unit of measurement.</p>

<p>All abbreviations should be explained ONLY the first time they appear in text in the following way:</p>

<p><b>Example:</b></p>

<p>.... following the main sample (MS). Therefore..........<br />
Note that <b>abstract, each figure and table</b> is considered a separate and independent part of the text and all abbreviations should be also explained there despite the fact that you may have already explained it text. If you have abbreviations that are repeated through tables and figures they should be explained in each figure and in each table (in heading or in footnote).</p>


<p><u><b>2.4. Latin names</b></u></p>

<p>Latin names of species, families etc. should be written in italics (even in titles). If you mention Latin names in your abstract they should be written in non italic since the rest of the text in abstract is in italic. The first time the name of a species appears in the text both genus and species must be present; later on in the text it is possible to use genus abbreviations.</p>

<p><b>Example:</b></p>

<p>First time appearing: <i>musculus biceps brachii</i></p>

<p>Abbreviated: <i>m. biceps brachii</i></p>
