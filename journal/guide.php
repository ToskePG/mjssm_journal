<p><u><b>1.16.2. Table footnote</b></u></p>

<p>Footnotes should be written below the table.</p>

<p><b>Examples:</b></p>

<p><sup>*</sup>p&gt;0.01, <sup>**</sup>p&gt;0.05, OPT – optimal intensity, MAX – maximal intensity, SR – resting state, SF – state of fatigue</p>

<p><b>SHOULD BE:</b> OPT – optimal intensity, MAX – maximal intensity<br />
<b>NOT:</b> OPT = optimal intensity, MAX = maximal intensity</p>

<p><u><b>1.16.3. Table citation</b></u></p>

<p>In the text, tables should be cited as full words. Some examples are given below:</p>

<p><b>Examples:</b></p>

<p><b>SHOULD BE:</b> Table 1 <br />
<b>NOT:</b> table 1 (first letter in caps)</p>

<p><b>SHOULD BE:</b> Table 1 <br />
<b>NOT:</b> Table 1. (no full stop)</p>

<p><b>SHOULD BE: </b>…(p&lt;0.05, Table 1). <b><br />
NOT: </b>...(p&lt;0.05). (Table 1)</p>

<p><b>SHOULD BE: </b>...as shown in Tables 1 and 3. (citing more tables at once)</p>

<p><b>SHOULD BE: </b>...result has shown (Tables 1-3) that... (citing more tables at once)</p>

<p><b>SHOULD BE: </b>....in our results (Tables 1, 2 and 5)... (citing more tables at once)</p>

<p><u><b>1.16.4. Subtables</b></u></p>

<p>Use this only if there is no other solution. It is preferred that you brake subtables in two independent tables or merge them into one.</p>

<p><b>Examples:</b></p>

<p>heading for a subtable: TABLE 1a</p>

<p>citation of a subtable: ....(Table 1a)</p>


<p><u><b>1.17. Figures</b></u></p>

<p>The only figures allowed in the main manuscript file are graphs and other Microsoft Office graphics. All other graphic material (pictures, photographs, drawings etc.) should be submitted separately in digitalized form. When submitting separate figure file s make sure that they are named properly to avoid any loss or confusion (See <b>Naming files</b>). On the last separate page of the main manuscript file, authors should place the headings of all the figures submitted separately. </p>

<p>All graphic material should be digitalized first. Avoid sending photographs or similar. All graphic materials should be of sufficient quality for print (300 dpi, or more if necessary). </p>

<p><b>Important!</b><br />
When submitting manuscript for the first time it is not necessary to send very high quality pictures. We are contacting all our reviewers via e-mail so if it is not extremely necessary, please resize your pictures. Complete submission package with all other files should not exceed 2 MB. </p>

<p>Figures should be numbered consecutively according to the order in which they appear in the text. If a figure has been published previously, acknowledge the original source and submit a written permission from the copyright holder to reproduce the material. Permission is required irrespective of authorship or publisher except for documents in the public domain. If photographs of people are used, either the subjects must not be identifiable or their pictures must be accompanied by written permission to use the photograph whenever possible permission for publication should be obtained.</p>

<p>Figures and figure headings should be completely intelligible without reference to the text. All abbreviations appearing in a figure or its heading not considered standard <b>MUST</b> be explained in a figure heading (see <b>Abbreviations</b>).</p>

<p>The format of figures should not exceed A4. Only exceptionally the format A3 will be accepted.</p>

<p>Avoid using any formatting (bold, underline) in figures or figure headings.</p>

<p>Be sure that each figure is cited in the text.</p>

<p><u><b>1.17.1. Graphs</b></u></p>

<p>All graphs and other Microsoft Office material should be placed in the main manuscript file each on a separate page and are accompanied by a corresponding figure heading.</p>

<p>All words for variable names should be capitalized.</p>

<p>All abbreviation s are to be explained in the footing of the figure. If you have “Number of….” then put the whole word NOT “No. of……”.</p>

<p><b>Axis titles</b> in your figures should have no formatting (bold, italic etc.). After the title of the axis there should be a unit of measure in parentheses for example Age (years). While the first letter of the axis title is capitalized, unit of measure is not. Whenever possible, all axis titles should be put below the middle of the axis.</p>

<p>Take care about the <b>colors</b> on your graphs. The graphs shall be printed in gray unless otherwise specified in the new submission form and paid for accordingly.</p>

<p>Decimal numbers on your axis should have a decimal full stop, NOT a comma.</p>

<p><u><b>1.17.2. Figure heading</b></u></p>

<p>Figures should not contain footnotes. All information, including explanations of abbreviations must be present in figure heading. Headings of the figures like graphs and other MS Office material submitted in the main manuscript file should be presented together with their corresponding figures. Headings of the figures submitted separately should be presented on the last separate page of the main manuscript file. </p>

<p>Figure headings are written in italics and begin with “<i>Fig.” </i>followed by figure number and a full stop. </p>

<p><b>Examples for figure heading:</b></p>

<p><i>Fig. 1. Changes in accuracy of instep football kick measured before and after fatigued. SR – resting state, SF – state of fatigue, <sup>*</sup>p&gt;0.01, <sup>**</sup>p&gt;0.05.</i></p>

<p><i>Fig. 7</i>. <i>Attitudes toward advertising through sport. </i>X<i> – mean, SD – standard deviation.</i></p>




