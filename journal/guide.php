			<p><u><b>1.1. General remarks</b></u></p>

<p>Authors should submit their manuscript as one Microsoft Word (.doc) file strictly following specified layout of manuscript structural elements. Main manuscript file should be carefully checked to contain all needed structural elements. Do not add any additional elements or data in your main manuscript file except the ones specified in guidelines! Do not break your main manuscript file in more files!</p>

<p>In text, avoid “one sentence – one paragraph” writing. Integrate your sentences in continuous text and sensible paragraphs.</p>

<p>Type the whole manuscript double-spaced!</p>

<p>Apart from chapter titles and subtitles avoid any kind of formatting (bold, underline) in the main text of the manuscript or table contents.</p>

<p><u><b>1.1.1. Length of the manuscript</b></u> </p>

<p>The manuscript should not exceed the maximum length of 8 pages as printed in Montenegrin Journal of Sports Science and Medicine, which are roughly 30 A4 pages of double-spaced text. Additional pages are extra charged (See <b>Costs and payments</b>)</p>

<p><u><b>1.1.2. Printing in color</b></u></p>

<p>If your manuscript contains pictures in color note that printing in color is extra charged. For prices see <b>Costs and Payments</b>. </p>


<p><u><b>1.2. Overview and position of structural elements of the main manuscript file</b></u></p>

<p><b>First or title page </b>contains: 1. title, 2. type of publication, 3. running head, 4. authors, and 5. affiliations.</p>

<p><b>Second page</b> contains: 6. abstract, and 7. key words.</p>

<p><b>Starting on a third page </b>follow: 8. chapters of the manuscript, 9. references 10. corresponding author's address, and 11. corresponding author's e-mail.</p>

<p><b>A separate page</b>, following corresponding author's address, contains: 12. naslov na crnogorskom jeziku (title in Montenegrin, only for Montenegrin authors), and 13. sažetak na crnogorskom jeziku (abstract in Montenegrin, only for Montenegrin authors).</p>
<p>Tables (14. Tables) with headings and footnotes should be put each on a separate page.</p>
<p>As for 15. Figures, <b>ONLY</b> graphs and other Microsoft Office graphics should be included in the main manuscript file, each on a separate page with appropriate heading below. All other graphic material should be submitted separately, in properly named files to avoid any confusion. The last, separate page of the manuscript should contain 16. Figure headings of figures submitted <b>separately</b>.</p>
