<p><b>GUIDELINES FOR AUTHORS</b></p>

<p>When preparing the final version of the manuscript, either NEW or REVISED authors should strictly follow these guidelines. Manuscripts departing substantially from the guidelines will be returned to the authors for revision.</p>

<p>For more detailed information on the uniform requirements of the manuscript elements please visit the web site of the International Committee of the Medical Journal Editors.</p>


